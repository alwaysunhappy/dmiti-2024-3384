def COM_NN_D(num1, num2):
    """
     Сравнивает два натуральных числа и возвращает:
      2 - если первое число больше второго,
      0 - если числа равны,
      1 - если первое число меньше второго.
    
     Args:
      num1: Первое натуральное число.
      num2: Второе натуральное число.
    """
    if num1 > num2:
        return 2  # Первое число больше
    elif num1 == num2:
        return 0  # Числа равны
    else:
        return 1  # Первое число меньше


if __name__ == "__main__":
    # Получение ввода от пользователя
    num1 = int(input("Введите первое натуральное число: "))
    num2 = int(input("Введите второе натуральное число: "))

    # Сравнение чисел и вывод результата
    result = COM_NN_D(num1, num2)
    print(f"Результат сравнения: {result}")
